// dos_filecrc
int CDOSLibApp::ads_dos_filecrc()
{
  CAdsArgs args;

  CDosPathString strPath;
  if (!args.GetPathString(strPath))
    return RSERR;

  strPath.FullPath();

  args.Next();

  if (args.IsEmpty())
  {
    FILE* pfile = _wfopen(strPath, L"rb");
    if (pfile)
    {
      UINT32 crc = Calc32Crc(pfile);
      fclose(pfile);
      CString str;
      str.Format(L"%08X", crc);
      acedRetStr(str);
    }
    else
      acedRetNil();
  }
  else
  {
    FILE* pfile = _wfopen(strPath, L"rb");
    if (pfile)
    {
      ULONG64 crc = Calc64Crc(pfile);
      fclose(pfile);
      CString str;
      str.Format(L"%016I64X", crc);
      acedRetStr(str);
    }
    else
      acedRetNil();
  }

  return RSRSLT;
}